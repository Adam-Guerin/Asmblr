class KnowledgeGraph:
    """Knowledge graph for AGI"""
    
    def __init__(self):
        self.nodes: dict[str, KnowledgeNode] = {}
        self.relationships: dict[str, list[str]] = defaultdict(list)
        self.domains: dict[KnowledgeDomain, list[str]] = defaultdict(list)
        
    def add_knowledge(self, node: KnowledgeNode) -> bool:
        """Add knowledge node to graph"""
        try:
            self.nodes[node.id] = node
            self.relationships[node.id] = node.relationships
            self.domains[node.domain].append(node.id)
            
            # Add reverse relationships
            for related_id in node.relationships:
                if related_id in self.nodes:
                    self.relationships[related_id].append(node.id)
            
            return True
            
        except Exception as e:
            logger.error(f"Error adding knowledge node: {e}")
            return False
    
    def find_related_concepts(self, concept_id: str, max_depth: int = 3) -> list[str]:
        """Find related concepts using graph traversal"""
        try:
            if concept_id not in self.nodes:
                return []
            
            related = []
            visited = set()
            queue = [(concept_id, 0)]
            
            while queue and len(related) < 100:
                current_id, depth = queue.pop(0)
                
                if current_id in visited or depth > max_depth:
                    continue
                
                visited.add(current_id)
                related.append(current_id)
                
                # Add related nodes to queue
                for related_id in self.relationships[current_id]:
                    if related_id not in visited:
                        queue.append((related_id, depth + 1))
            
            return related
            
        except Exception as e:
            logger.error(f"Error finding related concepts: {e}")
            return []
    
    def get_domain_knowledge(self, domain: KnowledgeDomain) -> list[KnowledgeNode]:
        """Get all knowledge in specific domain"""
        try:
            domain_nodes = []
            for node_id in self.domains[domain]:
                if node_id in self.nodes:
                    domain_nodes.append(self.nodes[node_id])
            
            return domain_nodes
            
        except Exception as e:
            logger.error(f"Error getting domain knowledge: {e}")
            return []
    
    def update_access_pattern(self, node_id: str):
        """Update access pattern for knowledge node"""
        try:
            if node_id in self.nodes:
                node = self.nodes[node_id]
                node.last_accessed = datetime.now()
                node.access_count += 1
                
                # Update confidence based on usage
                if node.access_count > 10:
                    node.confidence = min(1.0, node.confidence + 0.01)
            
        except Exception as e:
            logger.error(f"Error updating access pattern: {e}")

