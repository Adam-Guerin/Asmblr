class AlertNotificationManager:
    """Manages alert notifications across multiple channels."""
    
    def __init__(self):
        self.notifiers: dict[NotificationChannel, Any] = {}
        self.notification_configs: dict[NotificationChannel, NotificationConfig] = {}
        self.rate_limits: dict[str, datetime] = {}
        self.min_interval = timedelta(minutes=5)  # Minimum time between same alert notifications
    
    def add_notification_channel(self, config: NotificationConfig) -> bool:
        """Add a notification channel."""
        try:
            if config.channel == NotificationChannel.EMAIL:
                notifier = EmailNotifier(config.config)
            elif config.channel == NotificationChannel.WEBHOOK:
                notifier = WebhookNotifier(config.config)
            elif config.channel == NotificationChannel.SLACK:
                notifier = SlackNotifier(config.config)
            elif config.channel == NotificationChannel.DISCORD:
                notifier = DiscordNotifier(config.config)
            elif config.channel == NotificationChannel.CONSOLE:
                notifier = ConsoleNotifier()
            else:
                logger.error(f"Unsupported notification channel: {config.channel}")
                return False
            
            self.notifiers[config.channel] = notifier
            self.notification_configs[config.channel] = config
            
            logger.info(f"Added notification channel: {config.channel.value}")
            return True
            
        except Exception as e:
            logger.error(f"Failed to add notification channel {config.channel}: {e}")
            return False
    
    def remove_notification_channel(self, channel: NotificationChannel) -> None:
        """Remove a notification channel."""
        if channel in self.notifiers:
            del self.notifiers[channel]
        if channel in self.notification_configs:
            del self.notification_configs[channel]
        logger.info(f"Removed notification channel: {channel.value}")
    
    def send_alert(self, alert: Alert) -> dict[NotificationChannel, bool]:
        """Send alert through all enabled channels."""
        results = {}
        
        # Check rate limiting
        now = datetime.now()
        if alert.name in self.rate_limits:
            if now - self.rate_limits[alert.name] < self.min_interval:
                logger.debug(f"Alert {alert.name} rate limited")
                return results
        
        for channel, config in self.notification_configs.items():
            if not config.enabled:
                continue
            
            try:
                notifier = self.notifiers.get(channel)
                if notifier:
                    success = notifier.send_alert(alert)
                    results[channel] = success
                    
                    if success:
                        logger.info(f"Alert {alert.name} sent via {channel.value}")
                    else:
                        logger.error(f"Failed to send alert {alert.name} via {channel.value}")
                        
            except Exception as e:
                logger.error(f"Error sending alert {alert.name} via {channel.value}: {e}")
                results[channel] = False
        
        # Update rate limit
        self.rate_limits[alert.name] = now
        
        return results
    
    def test_notifications(self) -> dict[NotificationChannel, bool]:
        """Test all notification channels."""
        from .metrics import Alert, AlertSeverity
        
        test_alert = Alert(
            name="test_notification",
            severity=AlertSeverity.INFO,
            condition="test = true",
            threshold=1.0,
            message="This is a test notification from Asmblr"
        )
        
        return self.send_alert(test_alert)
    
    def get_channel_status(self) -> dict[str, Any]:
        """Get status of all notification channels."""
        status = {}
        
        for channel, config in self.notification_configs.items():
            status[channel.value] = {
                "enabled": config.enabled,
                "configured": channel in self.notifiers,
                "config": {k: v for k, v in config.config.items() if k not in ["password", "token"]}
            }
        
        return status


# Global notification manager instance
_global_notification_manager = AlertNotificationManager()


def get_notification_manager() -> AlertNotificationManager:
    """Get the global notification manager instance."""
    return _global_notification_manager


def setup_default_notifications() -> None:
    """Setup default notification channels from environment variables."""
    import os
    
    # Console notifications (always enabled)
    console_config = NotificationConfig(
        channel=NotificationChannel.CONSOLE,
        enabled=True
    )
    _global_notification_manager.add_notification_channel(console_config)
    
    # Email notifications
    if os.getenv("ALERT_EMAIL_ENABLED", "false").lower() == "true":
        email_config = NotificationConfig(
            channel=NotificationChannel.EMAIL,
            enabled=True,
            config={
                "smtp_server": os.getenv("ALERT_SMTP_SERVER", "localhost"),
                "smtp_port": int(os.getenv("ALERT_SMTP_PORT", "587")),
                "username": os.getenv("ALERT_EMAIL_USERNAME", ""),
                "password": os.getenv("ALERT_EMAIL_PASSWORD", ""),
                "from_email": os.getenv("ALERT_EMAIL_FROM", ""),
                "to_emails": os.getenv("ALERT_EMAIL_TO", "").split(","),
                "use_tls": os.getenv("ALERT_EMAIL_USE_TLS", "true").lower() == "true"
            }
        )
        _global_notification_manager.add_notification_channel(email_config)
    
    # Webhook notifications
    if os.getenv("ALERT_WEBHOOK_ENABLED", "false").lower() == "true":
        webhook_config = NotificationConfig(
            channel=NotificationChannel.WEBHOOK,
            enabled=True,
            config={
                "url": os.getenv("ALERT_WEBHOOK_URL", ""),
                "headers": json.loads(os.getenv("ALERT_WEBHOOK_HEADERS", "{}")),
                "timeout": int(os.getenv("ALERT_WEBHOOK_TIMEOUT", "30")),
                "retry_count": int(os.getenv("ALERT_WEBHOOK_RETRY_COUNT", "3"))
            }
        )
        _global_notification_manager.add_notification_channel(webhook_config)
    
    # Slack notifications
    if os.getenv("ALERT_SLACK_ENABLED", "false").lower() == "true":
        slack_config = NotificationConfig(
            channel=NotificationChannel.SLACK,
            enabled=True,
            config={
                "webhook_url": os.getenv("ALERT_SLACK_WEBHOOK_URL", ""),
                "channel": os.getenv("ALERT_SLACK_CHANNEL", "#alerts"),
                "username": os.getenv("ALERT_SLACK_USERNAME", "Asmblr Bot"),
                "icon_emoji": os.getenv("ALERT_SLACK_ICON_EMOJI", ":robot_face:")
            }
        )
        _global_notification_manager.add_notification_channel(slack_config)
    
    # Discord notifications
    if os.getenv("ALERT_DISCORD_ENABLED", "false").lower() == "true":
        discord_config = NotificationConfig(
            channel=NotificationChannel.DISCORD,
            enabled=True,
            config={
                "webhook_url": os.getenv("ALERT_DISCORD_WEBHOOK_URL", ""),
                "username": os.getenv("ALERT_DISCORD_USERNAME", "Asmblr Bot"),
                "avatar_url": os.getenv("ALERT_DISCORD_AVATAR_URL", "")
            }
        )
        _global_notification_manager.add_notification_channel(discord_config)
