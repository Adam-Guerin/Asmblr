class NotificationChannel(Enum):
    """Available notification channels."""
    EMAIL = "email"
    WEBHOOK = "webhook"
    SLACK = "slack"
    DISCORD = "discord"
    CONSOLE = "console"


@dataclass
class NotificationConfig:
    """Configuration for notification channels."""
    channel: NotificationChannel
    enabled: bool = True
    config: dict[str, Any] = None
    
    def __post_init__(self):
        if self.config is None:
            self.config = {}


class EmailNotifier:
    """Email notification handler."""
    
    def __init__(self, config: dict[str, Any]):
        self.smtp_server = config.get("smtp_server", "localhost")
        self.smtp_port = config.get("smtp_port", 587)
        self.username = config.get("username", "")
        self.password = config.get("password", "")
        self.from_email = config.get("from_email", "")
        self.to_emails = config.get("to_emails", [])
        self.use_tls = config.get("use_tls", True)
    
    def send_alert(self, alert: Alert) -> bool:
        """Send email alert."""
        try:
            msg = MimeMultipart()
            msg['From'] = self.from_email
            msg['To'] = ", ".join(self.to_emails)
            msg['Subject'] = f"[Asmblr Alert] {alert.name} - {alert.severity.value.upper()}"
            
            body = self._format_email_body(alert)
            msg.attach(MimeText(body, 'html'))
            
            server = smtplib.SMTP(self.smtp_server, self.smtp_port)
            if self.use_tls:
                server.starttls()
            
            if self.username and self.password:
                server.login(self.username, self.password)
            
            server.send_message(msg)
            server.quit()
            
            logger.info(f"Email alert sent for {alert.name}")
            return True
            
        except Exception as e:
            logger.error(f"Failed to send email alert: {e}")
            return False
    
    def _format_email_body(self, alert: Alert) -> str:
        """Format email body."""
        severity_colors = {
            AlertSeverity.INFO: "#17a2b8",
            AlertSeverity.WARNING: "#ffc107", 
            AlertSeverity.ERROR: "#dc3545",
            AlertSeverity.CRITICAL: "#6f42c1"
        }
        
        color = severity_colors.get(alert.severity, "#6c757d")
        
        return f"""
        <html>
        <body>
            <div style="font-family: Arial, sans-serif; max-width: 600px; margin: 0 auto;">
                <div style="background-color: {color}; color: white; padding: 20px; border-radius: 5px 5px 0 0;">
                    <h2 style="margin: 0;">🚨 Asmblr Alert</h2>
                    <p style="margin: 5px 0 0 0;">{alert.severity.value.upper()} - {alert.name}</p>
                </div>
                
                <div style="background-color: #f8f9fa; padding: 20px; border: 1px solid #dee2e6; border-top: none;">
                    <h3>Alert Details</h3>
                    <p><strong>Message:</strong> {alert.message}</p>
                    <p><strong>Condition:</strong> {alert.condition}</p>
                    <p><strong>Threshold:</strong> {alert.threshold}</p>
                    <p><strong>Trigger Count:</strong> {alert.trigger_count}</p>
                    <p><strong>Last Triggered:</strong> {alert.last_triggered.strftime('%Y-%m-%d %H:%M:%S') if alert.last_triggered else 'Never'}</p>
                </div>
                
                <div style="background-color: #e9ecef; padding: 15px; border: 1px solid #dee2e6; border-top: none; border-radius: 0 0 5px 5px;">
                    <p style="margin: 0; font-size: 12px; color: #6c757d;">
                        This alert was generated by Asmblr MVP Generator at {datetime.now().strftime('%Y-%m-%d %H:%M:%S')}.
                    </p>
                </div>
            </div>
        </body>
        </html>
        """


class WebhookNotifier:
    """Webhook notification handler."""
    
    def __init__(self, config: dict[str, Any]):
        self.url = config.get("url", "")
        self.headers = config.get("headers", {})
        self.timeout = config.get("timeout", 30)
        self.retry_count = config.get("retry_count", 3)
    
    def send_alert(self, alert: Alert) -> bool:
        """Send webhook alert."""
        try:
            payload = {
                "alert_name": alert.name,
                "severity": alert.severity.value,
                "message": alert.message,
                "condition": alert.condition,
                "threshold": alert.threshold,
                "trigger_count": alert.trigger_count,
                "last_triggered": alert.last_triggered.isoformat() if alert.last_triggered else None,
                "timestamp": datetime.now().isoformat(),
                "source": "asmblr"
            }
            
            for attempt in range(self.retry_count):
                try:
                    response = requests.post(
                        self.url,
                        json=payload,
                        headers=self.headers,
                        timeout=self.timeout
                    )
                    response.raise_for_status()
                    
                    logger.info(f"Webhook alert sent for {alert.name}")
                    return True
                    
                except requests.RequestException as e:
                    if attempt == self.retry_count - 1:
                        raise
                    logger.warning(f"Webhook attempt {attempt + 1} failed: {e}")
                    time.sleep(2 ** attempt)  # Exponential backoff
            
        except Exception as e:
            logger.error(f"Failed to send webhook alert: {e}")
            return False


